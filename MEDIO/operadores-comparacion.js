

//Comparadores Numericos
console.log(5 == 5);//comparacion Normal
console.log(5 == "5");//Comparacion implicita, osea que convierte el string en numero
console.log(5 == 10);//Comparacion logica normal

//Comparadores con Cadenas de texto
console.log("hola" == "hola");//Comparacion Normal
console.log("hola" == "polo");//Comparacion Normal
console.log("Test" == "test");//Comparacion Normal

//Comparadores con valores booleanos
console.log(true == true);//Comparacion Normal
console.log(true == false);//Compracion Normal
console.log(true == 1)//Comparacion Normal...devuelve verdadero por que true es equivalente a 1
console.log(false == 0)//Comparacion Normal...devuelve verdadero por que falso es equivalente a 0

//Compradores con otros valores
console.log(null == undefined);//Compracion Normal que devuelve true por que los dos son datos vacios
