//IIFE -----> Inmediately Invokated Function Expression

(function(){
    let mensaje = "Estoy probando la expresion de funcion invocada inmediatamente...";
    console.log(mensaje);
})();

(function(){
    let x = 50;
    let y = 500;
    let suma = x + y;
    console.log("La suma de "+x+" + "+y+" es: "+suma);
})();